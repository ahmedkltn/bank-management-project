    private void supprimerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_supprimerMouseClicked
        // TODO add your handling code here:
        if (this.inputValidation.validNumber(cin.getText())) {
            this.inputtedCIN = cin.getText();
            if (this.currentSession.deleteUser(this.inputtedCIN)) {
                error.setForeground(java.awt.Color.green);
                error.setText("Suppression du client est effectuer avec succ√®s !");
                this.cin.setText("");
            } else {
                error.setForeground(java.awt.Color.orange);
                error.setText("CIN non trouver");
            }
        } else {
            error.setForeground(java.awt.Color.red);
            error.setText("Merci d'entrer un CIN correct !");
            cin.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(255, 0, 0), 1, true));
        }
    }//GEN-LAST:event_supprimerMouseClicked

