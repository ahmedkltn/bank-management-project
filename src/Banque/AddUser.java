    private void addMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addMouseClicked
        // TODO add your handling code here:
        // TODO add your handling code here:
        String inputedNom = nom.getText();
        String inputedPrenom = prenom.getText();
        String inputedCIN = cin.getText();
        String inputedEmail = email.getText();
        String inputedNum = num.getText();
        boolean invalidInput = false;
        InputValidation validate = new InputValidation();
        double solde = 0;
        nom.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(0, 0, 0), 1, true));
        prenom.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(0, 0, 0), 1, true));
        cin.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(0, 0, 0), 1, true));
        email.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(0, 0, 0), 1, true));
        num.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(0, 0, 0), 1, true));
        if (!validate.validName(inputedNom)) {
            error.setText("Nom invalide !");
            nom.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(255, 0, 0), 1, true));
            invalidInput = true;
        }
        if (!validate.validName(inputedPrenom)) {
            error.setText("Prenom invalide !");
            prenom.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(255, 0, 0), 1, true));
            invalidInput = true;
        }
        if (!validate.validCIN(inputedCIN)) {
            error.setText("CIN invalide !");
            cin.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(255, 0, 0), 1, true));
            invalidInput = true;
        }
        if (!validate.validNumber(inputedNum)) {
            error.setText("Numero invalide !");
            num.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(255, 0, 0), 1, true));
            invalidInput = true;
        }
        if (!validate.validEmail(inputedEmail)) {
            error.setText("email invalide !");
            email.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(255, 0, 0), 1, true));
            invalidInput = true;
        }
        if (!invalidInput) {
            if (!currentSession.addClient(inputedNom, inputedPrenom, inputedCIN, inputedEmail, inputedNum, String.valueOf(solde))) {
                error.setText("CIN deja trouver !");
            } else {
                nom.setText("");
                prenom.setText("");
                cin.setText("");
                num.setText("");
                email.setText("");
                error.setForeground(new java.awt.Color(51, 255, 51));
                error.setText("Ajout avec succ√®s !");
            }
        }
    }//GEN-LAST:event_addMouseClicked

